package com.spideo.auction.status;

/**
 * The enum Auction status.
 */
public enum AuctionStatus {
    /**
     * Not started auction status.
     */
    NOT_STARTED,
    /**
     * Running auction status.
     */
    RUNNING,
    /**
     * Terminated auction status.
     */
    TERMINATED,
    /**
     * Deleted auction status.
     */
    DELETED
}
